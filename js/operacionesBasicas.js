export const sumaDosNumeros = (numUno,numDos)=> {
    return numUno + numDos
};

export const restaDosNumeros = (numUno,numDos)=> {
    return numUno - numDos
};
export const multiplicacionDosNumeros = (numUno,numDos)=> {
    return numUno * numDos
};
export const divisionDosNumeros = (numUno,numDos)=> {
    return numUno / numDos
};
